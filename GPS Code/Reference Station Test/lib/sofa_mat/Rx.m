function r = Rx(phi,R)
s = sin(phi);
c = cos(phi);

a10 =   c*R(2,1) + s*R(3,1);
a11 =   c*R(2,2) + s*R(3,2);
a12 =   c*R(2,3) + s*R(3,3);
a20 = - s*R(2,1) + c*R(3,1);
a21 = - s*R(2,2) + c*R(3,2);
a22 = - s*R(2,3) + c*R(3,3);

R(2,1) = a10;
R(2,2) = a11;
R(2,3) = a12;
R(3,1) = a20;
R(3,2) = a21;
R(3,3) = a22;
r=R;